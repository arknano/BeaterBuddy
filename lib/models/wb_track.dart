class WBTrack {
  final int id;
  final String title;
  final String pageURL;
  final String author;
  final int week;
  final int year;
  final String audioURL;

  WBTrack({
    required this.id,
    required this.title,
    required this.pageURL,
    required this.author,
    required this.week,
    required this.year,
    required this.audioURL,
  });
}
